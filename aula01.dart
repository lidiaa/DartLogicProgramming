main() {
  print("hello World"); //printar no console a frase hello world

  //criando variaveis
  var hello; //var chamada hello
  hello = "Hello Lidia"; //conteudo da variavel hello
  print(hello); //imprime Hello Lidia pela sua variavel hello

  //alterando valor da variavel
  hello = "Hello Maria"; //novo conteudo da variavel hello (sobrescreve)
  print(hello); //imprime Hello Maria pela sua variavel hello
}
