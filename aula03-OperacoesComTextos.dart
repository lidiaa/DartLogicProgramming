main() {
  //operando com textos em dart

  //concatenacao de strings
  var texto = "Hello" + "World"; //resultado: "HelloWorld"
  print(texto);

  texto = "Hello" + " " + "World"; //resultado: "Hello World"
  print(texto);

  //concatenando strings por variaveis
  var palavra1 = "Bom";
  var palavra2 = "Dia";
  texto = palavra1 + palavra2;
  print(texto);
}
